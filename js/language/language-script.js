function changeLanguage(lang) {
  let menu = document.querySelectorAll('.nav-link');
  let vision = document.querySelector('.vision')
  let visionContent = document.querySelector('.vision-content')
  let mission = document.querySelector('.mission')
  let missionContent1 = document.querySelector('.mission-content1')
  let missionContent2 = document.querySelector('.mission-content2')
  let values = document.querySelector('.values')
  let value = document.querySelectorAll('.value');
  let services = document.querySelector('.services')
  let service = document.querySelectorAll('.service');
  let ourStory = document.querySelector('.our-story')
  let ourStoryContent = document.querySelectorAll('.our-story-content');
  if(lang === 'english'){
    const keys = Object.keys(data.menu.english);
    const valueKeys = Object.keys(data.content.english.values);
    const serviceKeys = Object.keys(data.content.english.services);
    const ourStoryKeys = Object.keys(data.content.english.ourStory);
    for (let index = 0; index < menu.length; index++) {
      menu[index].textContent = data.menu.english[keys[index]]
    }
    vision.textContent = data.content.english.vision
    visionContent.textContent = data.content.english.visionContent
    mission.textContent = data.menu.english.visionAndMission
    missionContent1.textContent = data.content.english.ourMission.line1
    missionContent2.textContent = data.content.english.ourMission.line2
    for (let index = 0; index < value.length; index++) {
      value[index].textContent = data.content.english.values[valueKeys[index]]
    }
    values.textContent = data.menu.english.value
    for (let index = 0; index < service.length; index++) {
      service[index].textContent = data.content.english.services[serviceKeys[index]]
    }
    services.textContent = data.menu.english.services
    for (let index = 0; index < ourStoryContent.length; index++) {
      ourStoryContent[index].textContent = data.content.english.ourStory[ourStoryKeys[index]]
    }
    ourStory.textContent = data.menu.english.ourStory
  } 
  else if (lang === 'myanmar') {
    const keys = Object.keys(data.menu.myanmar);
    const valueKeys = Object.keys(data.content.myanmar.values);
    const serviceKeys = Object.keys(data.content.myanmar.services);
    const ourStoryKeys = Object.keys(data.content.myanmar.ourStory);
    for (let index = 0; index < menu.length; index++) {
      menu[index].textContent = data.menu.myanmar[keys[index]]
    }
    vision.textContent = data.content.myanmar.vision
    visionContent.textContent = data.content.myanmar.visionContent
    mission.textContent = data.menu.myanmar.visionAndMission
    missionContent1.textContent = data.content.myanmar.ourMission.line1
    missionContent2.textContent = data.content.myanmar.ourMission.line2
    for (let index = 0; index < value.length; index++) {
      value[index].textContent = data.content.myanmar.values[valueKeys[index]]
    }
    values.textContent = data.menu.myanmar.value
    for (let index = 0; index < service.length; index++) {
      service[index].textContent = data.content.myanmar.services[serviceKeys[index]]
    }
    services.textContent = data.menu.myanmar.services
    for (let index = 0; index < ourStoryContent.length; index++) {
      ourStoryContent[index].textContent = data.content.myanmar.ourStory[ourStoryKeys[index]]
    }
    ourStory.textContent = data.menu.myanmar.ourStory
  }
}